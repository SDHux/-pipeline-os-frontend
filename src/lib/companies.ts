export interface ICPBreakdown {
  fundingStage: number      // max 20
  growthRate: number        // max 20
  activelyHiring: number    // max 20
  categoryMatch: number     // max 15
  remoteOk: number          // max 10
  fortune1000Buyers: number // max 10
  compSize: number          // max 5
}

// ICP Scoring Logic
export function calculateICPScore(breakdown: ICPBreakdown): number {
  return Object.values(breakdown).reduce((sum, val) => sum + val, 0)
}

