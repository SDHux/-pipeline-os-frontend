export function getCompaniesByCategory(category: CompanyCategory): Company[] {
  return TARGET_COMPANIES.filter(c => c.category === category)
}

export function getTopCompanies(limit = 10): Company[] {
  return [...TARGET_COMPANIES].sort((a, b) => b.icpScore - a.icpScore).slice(0, limit)
}

export function searchCompanies(query: string): Company[] {
  const q = query.toLowerCase()
  return TARGET_COMPANIES.filter(c =>
    c.name.toLowerCase().includes(q) ||
    c.category.toLowerCase().includes(q) ||
    c.description.toLowerCase().includes(q) ||
    c.targetRoles.some(r => r.toLowerCase().includes(q))
  )
}
